#!/usr/bin/env node
// Build-time injector for telemetry provider config. Each app workspace runs
// this in its `prebuild`/`pretest` script. When env vars are absent (e.g. an
// OSS clone-and-build), the generated module exports nulls and telemetry
// silently disables itself. When env vars are present (CI / publish), the
// generated module carries the values; downstream code reads them at runtime.
//
// Usage: node scripts/inject-telemetry-config.mjs <cli|extension|docs>

import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const surface = process.argv[2];
if (!['cli', 'extension', 'docs'].includes(surface)) {
  console.error(
    'Usage: node scripts/inject-telemetry-config.mjs <cli|extension|docs>',
  );
  process.exit(1);
}

const REPO_ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');

const measurementId = process.env.GA4_MEASUREMENT_ID || null;
const apiSecret = process.env.GA4_MP_API_SECRET || null;

const TARGET = {
  cli: 'packages/apps/cli/src/generated/telemetry-config.ts',
  extension: 'packages/apps/extension/src/generated/telemetry-config.ts',
  docs: 'packages/apps/docs/src/generated/ga4-config.ts',
};

const target = join(REPO_ROOT, TARGET[surface]);
mkdirSync(dirname(target), { recursive: true });

const isDocs = surface === 'docs';
const body = isDocs
  ? `// Generated by scripts/inject-telemetry-config.mjs — do not edit.
export const GA4_MEASUREMENT_ID: string | null = ${JSON.stringify(measurementId)};
`
  : `// Generated by scripts/inject-telemetry-config.mjs — do not edit.
export const GA4_MEASUREMENT_ID: string | null = ${JSON.stringify(measurementId)};
export const GA4_MP_API_SECRET: string | null = ${JSON.stringify(apiSecret)};
`;

writeFileSync(target, body);
